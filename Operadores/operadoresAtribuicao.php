<?php

// Operadores de atribuição

$numA = 50;

// Soma
$numA += 25;

echo "Soma: " . $numA . PHP_EOL;

// Subtração
$numA -= 25;

echo "Subtração: " . $numA . PHP_EOL;

// Multiplicação
$numA *= 2;

echo "Multiplicação: " . $numA . PHP_EOL;

// Divisão
$numA /= 2;

echo "Divisão: " . $numA . PHP_EOL;

// Resto

$numA %= 2;

echo "Resto: " . $numA . PHP_EOL;