<?php

    //pi() [Mostra os principais valores de pi]
    echo pi();

    //min() [Retorna o menor número de uma lista de números]
    $numeros = [1, 2, 3, 4, 5, 6, 7, 8];
    echo min($numeros);
    //max() [Retorna o maior número de uma lista de números]
    echo max($numeros);

    //abs() [Retorna o valor positivo de um valor]
    echo abs(-10);

    //sqrt() [Retorna a raiz quadrada];
    echo sqrt(16);

    //round() [Arredonda valores decimais]
    echo round(10.6);

    //rand() [Retorna um número aleatório]
    echo rand();
    echo rand(1, 10); // Entre 1 e 10