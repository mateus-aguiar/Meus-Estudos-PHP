<?php


// Operador de igualdade ==
var_dump(1 == 5);
var_dump(1 == '1');

// Operador de diferença !=
var_dump(1 != 2);

// Operador idêntico ===
var_dump(1 === '1');
var_dump(1 === 1);

// Operador não idêntico !==
var_dump(1 !== 1);
var_dump(1 !== 2);

// Operador maior >
var_dump(5 > 1);

// Operador maior ou igual >=
var_dump(5 >= 5);

// Operador menor <
var_dump(5 < 10);

// Operador menor ou igual
var_dump(5 <= 5);